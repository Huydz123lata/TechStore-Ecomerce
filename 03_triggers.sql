-- 17. TRG_ADD_LOYALTY_PTS
CREATE OR REPLACE TRIGGER TRG_ADD_LOYALTY_PTS
AFTER UPDATE OF STATUS ON ORDERS
FOR EACH ROW
DECLARE
    v_points_earned NUMBER;
BEGIN
    IF :NEW.STATUS = 'DELIVERED' AND :OLD.STATUS <> 'DELIVERED' THEN
        -- Quy đổi: 10,000 VND = 1 điểm
        v_points_earned := FLOOR(:NEW.TOTAL / 10000);
        UPDATE LOYALTY_POINT SET TOTAL_POINTS = TOTAL_POINTS + v_points_earned WHERE CUSTOMER_ID = :NEW.CUSTOMER_ID;
    END IF;
END;
/

-- 18. TRG_WARRANTY_AUTO
CREATE OR REPLACE TRIGGER TRG_WARRANTY_AUTO
AFTER INSERT ON ORDER_DETAIL
FOR EACH ROW
DECLARE
    v_months NUMBER;
    v_serial VARCHAR2(50);
BEGIN
    SELECT WARRANTY_MONTH INTO v_months FROM PRODUCT WHERE PRODUCT_ID = :NEW.PRODUCT_ID;
    
    IF v_months > 0 THEN
        -- Khởi tạo vòng lặp sinh bảo hành dựa trên số lượng mua
        FOR i IN 1..:NEW.QUANTITY LOOP
            v_serial := 'SN' || TO_CHAR(SYSDATE, 'YYYYMMDDHH24MISS') || DBMS_RANDOM.STRING('U', 6) || i;
            INSERT INTO WARRANTY (SERIAL_NUMBER, STATUS, PRODUCT_ID, ORDER_DETAIL_ID, SOLD_AT)
            VALUES (v_serial, 1, :NEW.PRODUCT_ID, :NEW.ORDER_DETAIL_ID, SYSDATE);
        END LOOP;
    END IF;
END;
/

-- 19. TRG_REVIEW_VALIDATE
CREATE OR REPLACE TRIGGER TRG_REVIEW_VALIDATE
BEFORE INSERT ON REVIEW
FOR EACH ROW
DECLARE
    v_has_bought NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_has_bought
    FROM ORDERS o
    JOIN ORDER_DETAIL od ON o.ORDER_ID = od.ORDER_ID
    WHERE o.CUSTOMER_ID = :NEW.CUSTOMER_ID 
      AND od.PRODUCT_ID = :NEW.PRODUCT_ID 
      AND o.STATUS = 'DELIVERED';
      
    IF v_has_bought = 0 THEN
        RAISE_APPLICATION_ERROR(-20003, 'Khách hàng chưa mua hoặc chưa nhận sản phẩm này, không thể đánh giá!');
    END IF;
END;
/

