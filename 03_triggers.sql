/* AUTO TIMESTAMP TRIGGERS */

/* USERS */
CREATE OR REPLACE TRIGGER TRG_USERS_AUDIT
BEFORE INSERT OR UPDATE ON USERS
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        :NEW.CREATED_AT := SYSDATE;
    END IF;
    :NEW.UPDATED_AT := SYSDATE;
END;


