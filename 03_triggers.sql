-- 6. TRG_CART_ITEM_PRICE
CREATE OR REPLACE TRIGGER TRG_CART_ITEM_PRICE
BEFORE INSERT ON CART_ITEM
FOR EACH ROW
DECLARE
    v_price NUMBER(18,2);
BEGIN
    IF :NEW.PRICE IS NULL THEN
        SELECT PRICE INTO v_price FROM PRODUCT WHERE PRODUCT_ID = :NEW.PRODUCT_ID;
        :NEW.PRICE := v_price;
    END IF;
END;
/

-- 7. TRG_ORDER_TOTAL_CALC (Tránh Mutating Table bằng cách Update thẳng)
CREATE OR REPLACE TRIGGER TRG_ORDER_TOTAL_CALC
AFTER INSERT OR UPDATE OR DELETE ON ORDER_DETAIL
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        UPDATE ORDERS SET TOTAL = NVL(TOTAL, 0) + (:NEW.PRICE * :NEW.QUANTITY) 
        WHERE ORDER_ID = :NEW.ORDER_ID;
    ELSIF DELETING THEN
        UPDATE ORDERS SET TOTAL = NVL(TOTAL, 0) - (:OLD.PRICE * :OLD.QUANTITY) 
        WHERE ORDER_ID = :OLD.ORDER_ID;
    ELSIF UPDATING THEN
        UPDATE ORDERS SET TOTAL = NVL(TOTAL, 0) - (:OLD.PRICE * :OLD.QUANTITY) + (:NEW.PRICE * :NEW.QUANTITY) 
        WHERE ORDER_ID = :NEW.ORDER_ID;
    END IF;
END;
/

