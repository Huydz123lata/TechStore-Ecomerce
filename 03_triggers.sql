-- 9. TRG_DEDUCT_INVENTORY
CREATE OR REPLACE TRIGGER TRG_DEDUCT_INVENTORY
AFTER INSERT ON ORDER_DETAIL
FOR EACH ROW
DECLARE
    v_warehouse_id NUMBER(10);
BEGIN
    SELECT WAREHOUSE_ID INTO v_warehouse_id FROM ORDERS WHERE ORDER_ID = :NEW.ORDER_ID;
    
    IF v_warehouse_id IS NOT NULL THEN
        UPDATE INVENTORY 
        SET QUANTITY = QUANTITY - :NEW.QUANTITY
        WHERE PRODUCT_ID = :NEW.PRODUCT_ID AND WAREHOUSE_ID = v_warehouse_id;
    END IF;
END;
/

-- 10. TRG_RESTORE_INVENTORY
CREATE OR REPLACE TRIGGER TRG_RESTORE_INVENTORY
AFTER UPDATE OF STATUS ON ORDERS
FOR EACH ROW
BEGIN
    IF :NEW.STATUS = 'CANCELLED' AND :OLD.STATUS <> 'CANCELLED' THEN
        FOR rec IN (SELECT PRODUCT_ID, QUANTITY FROM ORDER_DETAIL WHERE ORDER_ID = :NEW.ORDER_ID)
        LOOP
            UPDATE INVENTORY 
            SET QUANTITY = QUANTITY + rec.QUANTITY
            WHERE PRODUCT_ID = rec.PRODUCT_ID AND WAREHOUSE_ID = :NEW.WAREHOUSE_ID;
        END LOOP;
    END IF;
END;
/

-- 11. TRG_PREVENT_DEL_PROD
CREATE OR REPLACE TRIGGER TRG_PREVENT_DEL_PROD
BEFORE UPDATE OF IS_DELETED ON PRODUCT
FOR EACH ROW
DECLARE
    v_total_stock NUMBER;
BEGIN
    IF :NEW.IS_DELETED = 1 THEN
        SELECT NVL(SUM(QUANTITY), 0) INTO v_total_stock FROM INVENTORY WHERE PRODUCT_ID = :OLD.PRODUCT_ID;
        IF v_total_stock > 0 THEN
            RAISE_APPLICATION_ERROR(-20001, 'Không thể xóa sản phẩm vẫn còn tồn kho!');
        END IF;
    END IF;
END;
/

-- 12. TRG_COUPON_EXPIRY_CHECK
CREATE OR REPLACE TRIGGER TRG_COUPON_EXPIRY_CHECK
BEFORE INSERT OR UPDATE OF COUPON_ID ON ORDERS
FOR EACH ROW
DECLARE
    v_end_at DATE;
    v_limit NUMBER;
    v_active NUMBER;
BEGIN
    IF :NEW.COUPON_ID IS NOT NULL THEN
        SELECT END_AT, USAGE_LIMIT, IS_ACTIVE INTO v_end_at, v_limit, v_active 
        FROM COUPON WHERE COUPON_ID = :NEW.COUPON_ID;
        
        IF v_active = 0 OR v_end_at < SYSDATE OR v_limit <= 0 THEN
            RAISE_APPLICATION_ERROR(-20002, 'Mã giảm giá đã hết hạn hoặc hết lượt sử dụng!');
        END IF;
    END IF;
END;
/

