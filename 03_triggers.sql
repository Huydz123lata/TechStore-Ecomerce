/** TRIGGER AUTO UPDATE UPDATED_AT */

CREATE OR REPLACE TRIGGER TRG_USERS_UPDATED
BEFORE UPDATE ON USERS
FOR EACH ROW
BEGIN
    :NEW.UPDATED_AT := SYSDATE;
END;

CREATE OR REPLACE TRIGGER TRG_PRODUCT_UPDATED
BEFORE UPDATE ON PRODUCT
FOR EACH ROW
BEGIN
    :NEW.UPDATED_AT := SYSDATE;
END;
