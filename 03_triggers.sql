-- 14. TRG_AFFILIATE_COMMISSION
CREATE OR REPLACE TRIGGER TRG_AFFILIATE_COMMISSION
AFTER UPDATE OF STATUS ON ORDERS
FOR EACH ROW
DECLARE
    v_aff_id NUMBER;
    v_rate NUMBER(5,2);
    v_commission NUMBER(18,2);
BEGIN
    IF :NEW.STATUS = 'DELIVERED' AND :OLD.STATUS <> 'DELIVERED' AND :NEW.COUPON_ID IS NOT NULL THEN
        BEGIN
            SELECT AFFILIATE_ID INTO v_aff_id FROM COUPON WHERE COUPON_ID = :NEW.COUPON_ID;
            IF v_aff_id IS NOT NULL THEN
                SELECT COMMISSION_RATE INTO v_rate FROM AFFILIATE WHERE AFFILIATE_ID = v_aff_id;
                v_commission := :NEW.TOTAL * (v_rate / 100);
                UPDATE AFFILIATE SET TOTAL_EARNED = TOTAL_EARNED + v_commission WHERE AFFILIATE_ID = v_aff_id;
            END IF;
        EXCEPTION
            WHEN NO_DATA_FOUND THEN NULL;
        END;
    END IF;
END;
/

