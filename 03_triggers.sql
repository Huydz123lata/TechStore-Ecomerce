/* AUTOMATION TRIGGER (5 trigger) */


/* Auto calculate order total */
CREATE OR REPLACE TRIGGER TRG_ORDER_TOTAL
AFTER INSERT OR UPDATE OR DELETE ON ORDER_DETAIL
FOR EACH ROW
DECLARE
    V_TOTAL NUMBER;
    V_ORDER NUMBER;
BEGIN

    IF INSERTING OR UPDATING THEN
        V_ORDER := :NEW.ORDER_ID;
    ELSE
        V_ORDER := :OLD.ORDER_ID;
    END IF;

    SELECT NVL(SUM(PRICE * QUANTITY),0)
    INTO V_TOTAL
    FROM ORDER_DETAIL
    WHERE ORDER_ID = V_ORDER;

    UPDATE ORDERS
    SET TOTAL = V_TOTAL
    WHERE ORDER_ID = V_ORDER;

END;

/* Auto apply promotion */
CREATE OR REPLACE TRIGGER TRG_PROMOTION_APPLY
BEFORE INSERT ON ORDER_DETAIL
FOR EACH ROW
DECLARE
    V_DISCOUNT NUMBER;
BEGIN

    SELECT DISCOUNT_VALUE
    INTO V_DISCOUNT
    FROM PROMOTION_PRODUCT
    WHERE PRODUCT_ID = :NEW.PRODUCT_ID
    AND ROWNUM = 1;

    :NEW.PRICE := :NEW.PRICE - V_DISCOUNT;

EXCEPTION
    WHEN NO_DATA_FOUND THEN NULL;

END;

/* Deduct inventory after payment */
CREATE OR REPLACE TRIGGER TRG_ORDER_DEDUCT_INVENTORY
AFTER UPDATE OF STATUS ON ORDERS
FOR EACH ROW
WHEN (NEW.STATUS = 'CONFIRMED')
BEGIN

    UPDATE INVENTORY I
    SET I.QUANTITY = I.QUANTITY - (
        SELECT OD.QUANTITY
        FROM ORDER_DETAIL OD
        WHERE OD.ORDER_ID = :NEW.ORDER_ID
        AND OD.PRODUCT_ID = I.PRODUCT_ID
    )
    WHERE I.PRODUCT_ID IN
    (
        SELECT PRODUCT_ID
        FROM ORDER_DETAIL
        WHERE ORDER_ID = :NEW.ORDER_ID
    );

END;

/* Auto update affiliate commission */
CREATE OR REPLACE TRIGGER TRG_AFFILIATE_COMMISSION
AFTER UPDATE OF PAYMENT_STATUS ON PAYMENT
FOR EACH ROW
WHEN (NEW.PAYMENT_STATUS = 'PAID')
BEGIN

    UPDATE AFFILIATE
    SET TOTAL_EARNED = TOTAL_EARNED + (:NEW.AMOUNT * COMMISSION_RATE / 100);

END;

