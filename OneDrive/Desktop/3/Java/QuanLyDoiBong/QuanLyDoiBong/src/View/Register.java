    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String userName = UserRegisField.getText();
        String password = passRegisField.getText();

        String passwordNew = HashUtil.hashPassword(password);
        DangKyProcess dk = new DangKyProcess();
        int tmp = dk.DangKyProcess(userName, passwordNew);
        if (tmp == 0) {
            JOptionPane optionPane = new JOptionPane("Đăng ký thất bại", JOptionPane.ERROR_MESSAGE);
            JDialog dialog = optionPane.createDialog("Thất bại");
            dialog.setAlwaysOnTop(true);
            dialog.setVisible(true);

        } else {
            JOptionPane optionPane = new JOptionPane("Đăng ký thành công", JOptionPane.INFORMATION_MESSAGE);
            JDialog dialog = optionPane.createDialog("Thành công");
            dialog.setAlwaysOnTop(true);
            dialog.setVisible(true);
            LoginForm loginform = new LoginForm();
            loginform.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

