    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        String selected = cbxTrangThai.getSelectedItem().toString();
        int statusInt = selected.equals("Hoạt động") ? 1 : 0;

        if (dao.updateCustomerStatus(this.customerAccountId, statusInt)) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Lỗi cập nhật!");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

