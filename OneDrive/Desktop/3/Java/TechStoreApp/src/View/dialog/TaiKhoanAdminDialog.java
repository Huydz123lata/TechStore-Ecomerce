    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // 1. Validation
        if (txtTenDangNhap.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên đăng nhập không được để trống!");
            return;
        }
        if (isAddMode && new String(txtMatKhau.getPassword()).trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mật khẩu cho tài khoản mới!");
            return;
        }

        // 2. Gom dữ liệu vào Model
        UserModel user = new UserModel();
        user.setUserId(this.currentUserId);
        user.setFullName(txtHoTen.getText());
        user.setSDT(txtSoDienThoai.getText());
        user.setEmail(txtEmail.getText());
        user.setAddress(txtDiaChi.getText());
        if (txtNgaySinh.getDate() != null) {
            user.setNgaySinh(new java.sql.Date(txtNgaySinh.getDate().getTime()));
        }
        user.setGioiTinh(cboGioiTinh.getSelectedItem().toString());
        user.setUserType(cboLoaiTaiKhoan.getSelectedItem().toString().equals("Quản trị viên") ? "ADMIN" : "STAFF");

        AccountModel acc = new AccountModel();
        acc.setAccountId(this.currentAccountId);
        acc.setUsername(txtTenDangNhap.getText());
        acc.setStatus(cboTrangThai.getSelectedItem().toString().equals("Hoạt động") ? 1 : 0);
        acc.setUserInfo(user);

        // Xử lý mật khẩu chỉ cho chế độ Thêm
        if (isAddMode) {
            String password = new String(txtMatKhau.getPassword());
            acc.setPasswordHash(HashUtil.hashPassword(password));
        }

        // 3. Thực thi
        if (isAddMode) {
            if (accDAO.addAdminStaffAccount(acc)) {
                JOptionPane.showMessageDialog(this, "Thêm tài khoản thành công!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi khi thêm! (Trùng Username/Email/SĐT)");
            }
        } else {
            if (accDAO.updateAdminStaffAccount(acc)) {
                JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật dữ liệu Oracle!");
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed

