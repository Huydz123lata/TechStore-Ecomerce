    public void setEditMode(AccountModel acc) {
        this.isAddMode = false;
        lblTitle.setText("CẬP NHẬT TÀI KHOẢN");

        // Lưu lại ID để tí nữa dùng trong câu lệnh WHERE của SQL
        this.currentAccountId = acc.getAccountId();
        this.currentUserId = acc.getUserId();

        // Đổ dữ liệu cá nhân
        UserModel user = acc.getUserInfo();
        txtHoTen.setText(user.getFullName());
        txtSoDienThoai.setText(user.getSDT());
        txtEmail.setText(user.getEmail());
        txtDiaChi.setText(user.getAddress());
        txtNgaySinh.setDate(user.getNgaySinh());
        cboGioiTinh.setSelectedItem(user.getGioiTinh());

        // Đổ dữ liệu tài khoản
        txtTenDangNhap.setText(acc.getUsername());
        txtTenDangNhap.setEditable(false); // Oracle thường không cho sửa Username để tránh lỗi quan hệ
        txtMatKhau.setText("********");    // Hiển thị giả lập, không sửa pass ở đây
        txtMatKhau.setEnabled(false);      // Muốn đổi pass nên làm một chức năng riêng cho an toàn

        // Đổ loại tài khoản và trạng thái
        cboLoaiTaiKhoan.setSelectedItem(user.getUserType().equals("ADMIN") ? "Quản trị viên" : "Nhân viên");
        cboTrangThai.setSelectedItem(acc.getStatus() == 1 ? "Hoạt động" : "Bị khóa");
    }

