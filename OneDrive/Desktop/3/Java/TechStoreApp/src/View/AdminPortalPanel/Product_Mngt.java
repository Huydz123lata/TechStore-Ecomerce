    private void applyPermissions() {
        String tenChucNang = AppConstants.FUNC_SAN_PHAM;

        if (UserSession.canDo(tenChucNang, "ADD") == 0) {
            btnThem.setVisible(false);
        }
        if (UserSession.canDo(tenChucNang, "EDIT") == 0) {
            btnSua.setVisible(false);
        }
        if (UserSession.canDo(tenChucNang, "DELETE") == 0) {
            btnXoa.setVisible(false);
        }
    }

