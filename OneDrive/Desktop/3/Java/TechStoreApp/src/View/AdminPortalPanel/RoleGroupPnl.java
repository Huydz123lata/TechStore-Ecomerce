    private void changeItem() {
        AccountSql account = new AccountSql();
        List<String> listName = account.getRoleGroupName();

        jComboBox1.removeAllItems();
        for (String name : listName) {
            jComboBox1.addItem(name);
        }
    }

    private void loadRolesToTable() {

        DefaultTableModel model = (DefaultTableModel) tblAssign.getModel();
        model.setRowCount(0);

        AccountSql account = new AccountSql();
        List<String> roles = account.getAllRoles();

        for (String roleName : roles) {
            model.addRow(new Object[]{false, roleName});
        }
    }

    public void loadTableRight() {
        DefaultTableModel model = (DefaultTableModel) tblRight.getModel();
        model.setRowCount(0);

        AccountSql accSql = new AccountSql();
        List<Vector> list = accSql.getRoleGroupCombined();

        for (Vector row : list) {
            model.addRow(row);
        }
    }
