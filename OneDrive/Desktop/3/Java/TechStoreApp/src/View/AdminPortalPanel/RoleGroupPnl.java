    private void myButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_myButton1ActionPerformed

        String selectedGroup = (String) jComboBox1.getSelectedItem();
        if (selectedGroup == null) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một nhóm!");
            return;
        }

        List<String> rolesToSave = new ArrayList<>();
        for (int i = 0; i < tblAssign.getRowCount(); i++) {
            Boolean isChecked = (Boolean) tblAssign.getValueAt(i, 0);
            if (isChecked != null && isChecked) {
                rolesToSave.add(tblAssign.getValueAt(i, 1).toString());
            }
        }

        AccountSql account = new AccountSql();
        boolean isOk = account.saveGroupAssign(selectedGroup, rolesToSave);

        if (isOk) {
            JOptionPane.showMessageDialog(this, "Đã cập nhật quyền cho nhóm " + selectedGroup);
            loadTableRight();
        } else {
            JOptionPane.showMessageDialog(this, "Lưu thất bại, kiểm tra lại kết nối!");
        }
    }//GEN-LAST:event_myButton1ActionPerformed

