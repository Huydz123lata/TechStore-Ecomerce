/* ================= PAYMENT ================= */
CREATE TABLE PAYMENT (
    PAYMENT_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PAYMENT_METHOD NVARCHAR2(30) NOT NULL CHECK (PAYMENT_METHOD IN ('COD','BANK_TRANSFER','MOMO','VNPAY')),
    PROVIDER NVARCHAR2(50),
    TRANSACTION_CODE NVARCHAR2(100) CONSTRAINT UQ_PAYMENT_TRANSACTION UNIQUE,
    AMOUNT NUMBER(18,2)
        CONSTRAINT CHK_PAYMENT_AMOUNT CHECK (AMOUNT >= 0),
    PAYMENT_STATUS NVARCHAR2(20) CHECK (PAYMENT_STATUS IN ('PENDING','SUCCESS','FAILED','REFUNDED')),
    ORDER_ID NUMBER(10),
    PAID_AT DATE,
    CREATED_AT DATE DEFAULT SYSDATE,
    UPDATED_AT DATE DEFAULT SYSDATE,
    IS_DELETED NUMBER(1) DEFAULT 0  CHECK (IS_DELETED IN (0,1))
);

/* ================= COUPON ================= */
CREATE TABLE COUPON (
    COUPON_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CODE NVARCHAR2(30) NOT NULL
        CONSTRAINT UQ_COUPON_CODE UNIQUE,
    DISCOUNT_TYPE NVARCHAR2(10) NOT NULL CHECK (DISCOUNT_TYPE IN ('PERCENTAGE','AMOUNT')),
    DISCOUNT_VALUE NUMBER(18,2) NOT NULL CHECK (DISCOUNT_VALUE >= 0),
    MIN_ORDER_VALUE NUMBER(18,2) DEFAULT 0 CHECK (MIN_ORDER_VALUE >= 0),
    MAX_DISCOUNT NUMBER(18,2) CHECK (MAX_DISCOUNT >= 0),
    START_AT DATE NOT NULL,
    END_AT DATE NOT NULL,
    IS_ACTIVE NUMBER(1) DEFAULT 1 CHECK (IS_ACTIVE IN (0,1)),
    DESCRIPTION NVARCHAR2(255),
    USAGE_LIMIT NUMBER(10) CHECK (USAGE_LIMIT >= 0),
    AFFILIATE_ID NUMBER(10),
    CREATED_AT DATE DEFAULT SYSDATE,
    UPDATED_AT DATE DEFAULT SYSDATE,
    IS_DELETED NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);
/* ================= AFFILIATE ================= */
