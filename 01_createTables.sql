/* ================= WAREHOUSE ================= */
CREATE TABLE WAREHOUSE (
    WAREHOUSE_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME NVARCHAR2(100) NOT NULL,
    ADDRESS NVARCHAR2(255) NOT NULL,
    PHONE NVARCHAR2(20),
    CREATED_AT DATE DEFAULT SYSDATE,
    UPDATED_AT DATE DEFAULT SYSDATE,
    IS_DELETED NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);

/* ================= INVENTORY ================= */
CREATE TABLE INVENTORY (
    INVENTORY_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRODUCT_ID NUMBER(10) NOT NULL,
    WAREHOUSE_ID NUMBER(10) NOT NULL,
    QUANTITY NUMBER(10) DEFAULT 0 CHECK (QUANTITY >= 0),
    CREATED_AT DATE DEFAULT SYSDATE,
    UPDATED_AT DATE DEFAULT SYSDATE,
    IS_DELETED NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1)),

    /* Một sản phẩm trong một kho chỉ nên có 1 dòng tồn kho. */
     CONSTRAINT UQ_INVENTORY_PRODUCT_WAREHOUSE
     UNIQUE (PRODUCT_ID, WAREHOUSE_ID)
);
/* ================= ORDER ================= */
CREATE TABLE ORDERS (
    ORDER_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TOTAL NUMBER(18,2) NOT NULL,
    SHIPPING_FEE NUMBER(18,2) DEFAULT 0,
    STATUS VARCHAR2(30) NOT NULL CHECK (STATUS IN ('PENDING','CONFIRMED','PROCESSING','SHIPPING','DELIVERED','CANCELLED')),
    CUSTOMER_ID NUMBER(10),
    COUPON_ID NUMBER(10),
    CREATED_AT DATE DEFAULT SYSDATE
);
/* ================= ORDER DETAIL ================= */
CREATE TABLE ORDER_DETAIL (
    ORDER_DETAIL_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ORDER_ID NUMBER(10),
    PRODUCT_ID NUMBER(10),
    QUANTITY NUMBER(10) CHECK (QUANTITY > 0),
    PRICE NUMBER(18,2) CHECK (PRICE > 0)
);

/* ================= GROUP BUY ================= */
CREATE TABLE GROUP_BUY (
    GROUP_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MIN_PARTICIPANTS NUMBER(5) NOT NULL CHECK (MIN_PARTICIPANTS > 1),
    CUR_PARTICIPANTS NUMBER(5) DEFAULT 0 CHECK (CUR_PARTICIPANTS >= 0),
    DISCOUNT_PERCENT NUMBER(5,2) NOT NULL CHECK (DISCOUNT_PERCENT BETWEEN 0 AND 100),
    STATUS VARCHAR2(20) DEFAULT 'WAITING' CHECK (STATUS IN ('WAITING','SUCCESS','CANCELLED','EXPIRED')),
    PRODUCT_ID NUMBER(10) NOT NULL,
    START_AT DATE DEFAULT SYSDATE,
    EXPIRY_TIME DATE NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE,
    UPDATED_AT DATE DEFAULT SYSDATE
);

/* ================= GROUP MEMBER ================= */
CREATE TABLE GROUP_MEMBER (
