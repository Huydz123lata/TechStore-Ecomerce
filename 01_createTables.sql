/* ================= REVIEW ================= */
CREATE TABLE REVIEW (
    REVIEW_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RATING NUMBER(1) NOT NULL CHECK (RATING BETWEEN 1 AND 5),
    TITLE VARCHAR2(20),
    CONTENT VARCHAR2(200),
    IS_APPROVED NUMBER(1) DEFAULT 0 CHECK (IS_APPROVED IN (0,1)),
