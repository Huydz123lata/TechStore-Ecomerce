       AND :OLD.STATUS <> 'SUCCESS' THEN
        IF :NEW.CURRENT_MEMBER < :NEW.TARGET_MEMBER THEN
            RAISE_APPLICATION_ERROR(-20012, 'Chua du thanh vien de xac nhan thanh cong');
        END IF;
    END IF;
END;
