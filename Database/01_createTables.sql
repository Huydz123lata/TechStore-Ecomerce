/* -------------------------------------------------------------------------
   PHẦN 1: TẠO CÁC BẢNG (TABLES)
   ------------------------------------------------------------------------- */

-- 1. Thông tin người dùng
CREATE TABLE "USER" (
    USER_ID      NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FULL_NAME    VARCHAR2(100) NOT NULL,
    PHONE_NUMBER VARCHAR2(20) UNIQUE NOT NULL,
    ADDRESS      VARCHAR2(255),
    NGAY_SINH    DATE,
    GIOI_TINH    VARCHAR2(10) CHECK (GIOI_TINH IN ('Nam', 'Nu')),
    USER_TYPE    VARCHAR2(20) DEFAULT 'CUSTOMER' CHECK (USER_TYPE IN ('ADMIN', 'STAFF', 'CUSTOMER')),
    CREATED_AT   DATE DEFAULT SYSDATE,
    UPDATED_AT   DATE DEFAULT SYSDATE,
    IS_DELETED   NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);

-- 2. Tài khoản
CREATE TABLE ACCOUNT (
    ACCOUNT_ID    NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID       NUMBER(10) NOT NULL,
    USERNAME      VARCHAR2(50) UNIQUE NOT NULL,
    PASSWORD_HASH VARCHAR2(200) NOT NULL,
    STATUS        VARCHAR2(20) DEFAULT 'ACTIVE',
    CREATED_AT    DATE DEFAULT SYSDATE,
    UPDATED_AT    DATE DEFAULT SYSDATE,
    IS_DELETED    NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);

-- 3. Phân hệ chức năng
CREATE TABLE FUNCTION (
    FUNCTION_ID   NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME_FUNCTION VARCHAR2(100) NOT NULL,
    CREATED_AT    DATE DEFAULT SYSDATE
);

-- 4. Quyền chi tiết (Add, Edit, Delete, View)
CREATE TABLE ROLE (
    ROLE_ID       NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FUNCTION_ID   NUMBER(10) NOT NULL,
    ROLE_NAME     VARCHAR2(100) NOT NULL,
    ADD_PERM      NUMBER(1) DEFAULT 0 CHECK (ADD_PERM IN (0,1)),
    EDIT_PERM     NUMBER(1) DEFAULT 0 CHECK (EDIT_PERM IN (0,1)),
    DELETE_PERM   NUMBER(1) DEFAULT 0 CHECK (DELETE_PERM IN (0,1)),
    VIEW_PERM     NUMBER(1) DEFAULT 0 CHECK (VIEW_PERM IN (0,1)),
    CREATED_AT    DATE DEFAULT SYSDATE,
    IS_DELETED    NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);

-- 5. Nhóm quyền (Role Group)
CREATE TABLE ROLE_GROUP (
    ROLE_GROUP_ID   NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME_ROLE_GROUP VARCHAR2(100) NOT NULL,
    CREATED_AT      DATE DEFAULT SYSDATE,
    IS_DELETED      NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);

-- 6. Gán Quyền vào Nhóm
CREATE TABLE ROLE_GROUP_ASSIGN_ROLE (
    ROLE_GROUP_ID NUMBER(10) NOT NULL,
    ROLE_ID       NUMBER(10) NOT NULL,
    PRIMARY KEY (ROLE_GROUP_ID, ROLE_ID)
);

-- 7. Gán Tài khoản vào Nhóm
CREATE TABLE ACCOUNT_ASSIGN_ROLE_GROUP (
    ACCOUNT_ID    NUMBER(10) NOT NULL,
    ROLE_GROUP_ID NUMBER(10) NOT NULL,
    PRIMARY KEY (ACCOUNT_ID, ROLE_GROUP_ID)
);

-- 8. Gán Quyền trực tiếp cho Tài khoản
CREATE TABLE ACCOUNT_ASSIGN_ROLE (
    ACCOUNT_ID NUMBER(10) NOT NULL,
    ROLE_ID    NUMBER(10) NOT NULL,
    PRIMARY KEY (ACCOUNT_ID, ROLE_ID)
);

-- 9. Token phiên làm việc
CREATE TABLE ACCOUNT_TOKEN (
    TOKEN_ID      NUMBER(20) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ACCOUNT_ID    NUMBER(10) NOT NULL,
    TOKEN_VALUE   VARCHAR2(500) UNIQUE NOT NULL,
    EXPIRES_AT    DATE NOT NULL,
    ISSUED_AT     DATE DEFAULT SYSDATE,
    IS_REVOKED    CHAR(1) DEFAULT 'N' CHECK (IS_REVOKED IN ('Y', 'N'))
);

-- 10. Danh mục sản phẩm (Cha-Con)
CREATE TABLE CATEGORY (
    CATEGORY_ID        NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME               VARCHAR2(40) NOT NULL,
    PARENT_CATEGORY_ID NUMBER(10),
    CREATED_AT         DATE DEFAULT SYSDATE,
    IS_DELETED         NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);

-- 11. Sản phẩm
CREATE TABLE PRODUCT (
    PRODUCT_ID     NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME           VARCHAR2(100) NOT NULL,
    DESCRIPTION    VARCHAR2(255),
    PRICE          NUMBER(18,2) NOT NULL CHECK (PRICE >= 0),
    BRAND          VARCHAR2(50) NOT NULL,
    STATUS         NUMBER(1) DEFAULT 1 CHECK (STATUS IN (0,1)),
    WARRANTY_MONTH NUMBER(3) NOT NULL,
    CATEGORY_ID    NUMBER(10) NOT NULL,
    CREATED_AT     DATE DEFAULT SYSDATE,
    UPDATED_AT     DATE DEFAULT SYSDATE,
    IS_DELETED     NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);

-- 12. Kho hàng
CREATE TABLE WAREHOUSE (
    WAREHOUSE_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME         VARCHAR2(100) NOT NULL,
    ADDRESS      VARCHAR2(255) NOT NULL,
    CREATED_AT   DATE DEFAULT SYSDATE,
    IS_DELETED   NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);

-- 13. Tồn kho chi tiết
CREATE TABLE INVENTORY (
    PRODUCT_ID   NUMBER(10) NOT NULL,
    WAREHOUSE_ID NUMBER(10) NOT NULL,
    QUANTITY     NUMBER(10) DEFAULT 0 CHECK (QUANTITY >= 0),
    UPDATED_AT   DATE DEFAULT SYSDATE,
    PRIMARY KEY (PRODUCT_ID, WAREHOUSE_ID)
);

-- 14. Đơn hàng
CREATE TABLE ORDERS (
    ORDER_ID         NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID          NUMBER(10) NOT NULL,
    COUPON_ID        NUMBER(10),
    TOTAL            NUMBER(18,2) DEFAULT 0 NOT NULL,
    SHIPPING_FEE     NUMBER(18,2) DEFAULT 0,
    STATUS           VARCHAR2(30) NOT NULL CHECK (STATUS IN ('PENDING','CONFIRMED','PROCESSING','SHIPPING','DELIVERED','CANCELLED')),
    RECEIVER_NAME    VARCHAR2(100) NOT NULL,
    RECEIVER_PHONE   VARCHAR2(20) NOT NULL,
    SHIPPING_ADDRESS VARCHAR2(255) NOT NULL,
    TRACKING_NUMBER  VARCHAR2(50),
    CREATED_AT       DATE DEFAULT SYSDATE,
    UPDATED_AT       DATE DEFAULT SYSDATE,
    IS_DELETED       NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);

-- 15. Chi tiết đơn hàng
CREATE TABLE ORDER_DETAIL (
    ORDER_DETAIL_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ORDER_ID        NUMBER(10) NOT NULL,
    PRODUCT_ID      NUMBER(10) NOT NULL,
    WAREHOUSE_ID    NUMBER(10),
    QUANTITY        NUMBER(10) NOT NULL CHECK (QUANTITY > 0),
    PRICE           NUMBER(18,2) NOT NULL CHECK (PRICE > 0)
);

-- 16. Thanh toán
CREATE TABLE PAYMENT (
    PAYMENT_ID       NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ORDER_ID         NUMBER(10) NOT NULL,
    PAYMENT_METHOD   VARCHAR2(30) NOT NULL,
    TRANSACTION_CODE VARCHAR2(100) UNIQUE,
    AMOUNT           NUMBER(18,2) NOT NULL,
    PAYMENT_STATUS   VARCHAR2(20) DEFAULT 'PENDING',
    PAID_AT          DATE,
    CREATED_AT       DATE DEFAULT SYSDATE
);

-- 17. Mã giảm giá (Coupon)
CREATE TABLE COUPON (
    COUPON_ID       NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CODE            VARCHAR2(40) NOT NULL UNIQUE,
    DISCOUNT_VALUE  NUMBER(18,2) NOT NULL,
    START_AT        DATE NOT NULL,
    END_AT          DATE NOT NULL,
    IS_ACTIVE       NUMBER(1) DEFAULT 1,
    CREATED_AT      DATE DEFAULT SYSDATE,
    IS_DELETED      NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);

-- 18. Chiến dịch khuyến mãi
CREATE TABLE PROMOTION (
    PROMOTION_ID   NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PROMOTION_NAME VARCHAR2(120) NOT NULL,
    START_AT       DATE NOT NULL,
    END_AT         DATE NOT NULL,
    IS_ACTIVE      NUMBER(1) DEFAULT 1,
    CREATED_AT     DATE DEFAULT SYSDATE,
    IS_DELETED     NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);

-- 19. Sản phẩm thuộc khuyến mãi
CREATE TABLE PROMOTION_PRODUCT (
    PROMOTION_ID   NUMBER(10) NOT NULL,
    PRODUCT_ID     NUMBER(10) NOT NULL,
    DISCOUNT_VALUE NUMBER(18,2),
    PRIMARY KEY (PROMOTION_ID, PRODUCT_ID)
);

-- 20. Đánh giá
CREATE TABLE REVIEW (
    REVIEW_ID   NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID     NUMBER(10) NOT NULL,
    PRODUCT_ID  NUMBER(10) NOT NULL,
    RATING      NUMBER(1) CHECK (RATING BETWEEN 1 AND 5),
    CONTENT     VARCHAR2(500),
    CREATED_AT  DATE DEFAULT SYSDATE,
    IS_DELETED  NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);

-- 21. Giỏ hàng
CREATE TABLE CART (
    CART_ID    NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID    NUMBER(10) NOT NULL,
    STATUS     VARCHAR2(20) DEFAULT 'ACTIVE',
    CREATED_AT DATE DEFAULT SYSDATE
);

-- 22. Chi tiết giỏ hàng
CREATE TABLE CART_ITEM (
    CART_ITEM_ID NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CART_ID      NUMBER(10) NOT NULL,
    PRODUCT_ID   NUMBER(10) NOT NULL,
    QUANTITY     NUMBER(10) NOT NULL CHECK (QUANTITY > 0),
    CONSTRAINT UQ_CART_PRODUCT UNIQUE (CART_ID, PRODUCT_ID)
);

-- 23. Điểm tích lũy
CREATE TABLE LOYALTY_POINT (
    USER_ID      NUMBER(10) NOT NULL PRIMARY KEY,
    TOTAL_POINTS NUMBER(10) DEFAULT 0,
    UPDATED_AT   DATE DEFAULT SYSDATE
);

-- 24. Mua chung (Group Buy - Thiết kế theo mã mời )
CREATE TABLE GROUP_BUY (
    GROUP_BUY_ID   NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRODUCT_ID     NUMBER(10) NOT NULL,
    HOST_USER_ID   NUMBER(10) NOT NULL, -- Trưởng nhóm
    INVITE_CODE    VARCHAR2(20) UNIQUE NOT NULL, -- Mã mời cho người khác join
    TARGET_MEMBER  NUMBER(10) NOT NULL CHECK (TARGET_MEMBER > 1),
    CURRENT_MEMBER NUMBER(10) DEFAULT 1,
    DISCOUNT_PRICE NUMBER(18,2) NOT NULL,
    EXPIRES_AT     DATE NOT NULL,
    STATUS         VARCHAR2(20) DEFAULT 'PROCESSING' CHECK (STATUS IN ('PROCESSING', 'SUCCESS', 'FAILED')),
    CREATED_AT     DATE DEFAULT SYSDATE
);

-- 25. Thành viên tham gia nhóm mua chung
CREATE TABLE GROUP_BUY_PARTICIPANT (
    GROUP_BUY_ID NUMBER(10) NOT NULL,
    USER_ID      NUMBER(10) NOT NULL,
    ORDER_ID     NUMBER(10),
    JOINED_AT    DATE DEFAULT SYSDATE,
    PRIMARY KEY (GROUP_BUY_ID, USER_ID)
);

