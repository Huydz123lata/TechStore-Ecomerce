15. ORDER_DETAIL (Chi tiết đơn)      16. PAYMENT (Thanh toán)
17. COUPON (Mã giảm giá)             18. PROMOTION (Khuyến mãi)
19. PROMOTION_PRODUCT                20. REVIEW (Đánh giá)
21. CART (Giỏ hàng)                  22. CART_ITEM (Chi tiết giỏ)
23. LOYALTY_POINT (Điểm thưởng)      24. GROUP_BUY (Mua chung - Mã mời)
