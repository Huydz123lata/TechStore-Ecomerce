-- 12. Đối tác Affiliate
CREATE TABLE AFFILIATE (
    AFFILIATE_ID    NUMBER(10) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID         NUMBER(10) NOT NULL,
    COMMISSION_RATE NUMBER(5,2) DEFAULT 5.0,
    TOTAL_EARNED    NUMBER(18,2) DEFAULT 0,
    STATUS          VARCHAR2(20) DEFAULT 'ACTIVE',
    CREATED_AT      DATE DEFAULT SYSDATE,
    UPDATED_AT      DATE DEFAULT SYSDATE,
    IS_DELETED      NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1))
);

-- 13. Mã giảm giá (Coupon)
CREATE TABLE COUPON (
    COUPON_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CODE             NVARCHAR2(30) NOT NULL UNIQUE,
    DISCOUNT_TYPE    NVARCHAR2(10) NOT NULL CHECK (DISCOUNT_TYPE IN ('PERCENTAGE', 'AMOUNT')),
    DISCOUNT_VALUE   DECIMAL(18,2) NOT NULL CHECK (DISCOUNT_VALUE >= 0),
    MIN_ORDER_VALUE  DECIMAL(18,2) DEFAULT 0 CHECK (MIN_ORDER_VALUE >= 0),
    MAX_DISCOUNT     DECIMAL(18,2) CHECK (MAX_DISCOUNT >= 0),
    START_AT         DATE NOT NULL,
    END_AT           DATE NOT NULL,
    IS_ACTIVE        NUMBER(1) DEFAULT 1 CHECK (IS_ACTIVE IN (0,1)),
    DESCRIPTION      NVARCHAR2(255),
    AFFILIATE_ID     NUMBER(10),
    CREATED_AT       DATE DEFAULT SYSDATE,
    UPDATED_AT       DATE DEFAULT SYSDATE,
    IS_DELETED       NUMBER(1) DEFAULT 0 CHECK (IS_DELETED IN (0,1)),
    CONSTRAINT CHK_COUPON_DATES CHECK (END_AT > START_AT)
);

